let filtroAtual = "todos";

function filtrar(cat, btn) {
  filtroAtual = cat;
  document
    .querySelectorAll(".fn-nav button")
    .forEach((b) => b.classList.remove("active"));
  btn.classList.add("active");
  renderizar();
}

function renderizar() {
  const container = document.getElementById("articles");
  const banner = document.querySelector(".fn-banner");
  if (filtroAtual === "todos"){
    banner.style.display = "block";
  }else{
    banner.style.display = "none";
  }

  const lista =
    filtroAtual === "todos"
      ? noticias
      : noticias.filter((n) => n.cat === filtroAtual);

  container.innerHTML = lista
    .map((n) => {
      const thumb = n.imagem
        ? `<img src="${n.imagem}" alt="${n.tag}">`
        : `<span class="fn-thumb-fallback">${n.emoji || "📰"}</span>`;

      return `
        <article class="fn-card">
          <div class="fn-thumb">
            ${thumb}
          </div>
          <div class="fn-content">
            <span class="fn-tag">${n.tag}</span>
            <h2>${n.titulo}</h2>
            <p>${n.resumo}</p>
            <div class="fn-meta">
              ${n.data} &nbsp;·&nbsp;
              <a href="${n.url}" target="_blank" rel="noopener noreferrer">
                Leia mais em ${n.fonte} <i class="bi bi-arrow-up-right" aria-hidden="true"></i>
              </a>
            </div>
          </div>
        </article>
      `;
    })
    .join("");
}
renderizar();
